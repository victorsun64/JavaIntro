package javaBasics;

public class SchleifenBasics {

	public static void main(String[] args) {
		
		// FOR Schleife
		for (int i = 0; i < 5; i =i+1)
			System.out.println(i);
		
		//
		System.out.println("-----");
		int j =1;
		for (;j<10;) {
			System.out.println(j);
			j = j +1;
		}
		
	}

}
